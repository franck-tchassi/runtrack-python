def calcule(num1, operator, num2):
    if operator == '/':
        print(num1 / num2)
    elif operator == '*':
        print(num1 * num2)
    elif operator == '-':
        print(num1 - num2)
    elif operator == '%':
        print(num1 % num2)
    else:
        print(num1 + num2)

calcule(5, '/', 8)
calcule(5, '*', 8)
calcule(5, '-', 8)
calcule(5, '%', 8)
calcule(5, '+', 8)
    
   


    



